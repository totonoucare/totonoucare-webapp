function inferProductRole({ category, normalized, policyKey }) {
  const explicit = normalized?.productRole;
  if (explicit && PRODUCT_ROLE_META[explicit]) return explicit;

  const keyword = cleanKeyword(normalized?.keyword);
  const text = `${keyword} ${uniqueStrings(normalized?.tags).join(" ")}`;
  const intentType = normalized?.intentType || null;
  const preferred = asArray(normalized?.preferredProductTypes);

  if (category === "eat") {
    if (intentType === "light_meal" || preferred.includes("soupMeal") || /(味噌汁|みそ汁|スープ|雑炊|おかゆ|リゾット|惣菜|宅食|ミール)/.test(text)) {
      return /(常温|保存|備蓄|フリーズドライ|レトルト)/.test(text) ? "pantry_soup" : "light_meal";
    }
    if (intentType === "nutrition_support" || preferred.includes("supplement") || /(プロテイン|ビタミン|ミネラル|乳酸菌|酪酸菌|食物繊維|GABA|テアニン|グリシン|マグネシウム|鉄分)/.test(text)) return "nutrition_support";
    if (intentType === "ingredient" || preferred.includes("yakuzenIngredient") || /(なつめ|棗|クコ|枸杞|陳皮|白きくらげ|生姜|しょうが|桂皮|シナモン|よもぎ|菊花|桑葉|山査子)/.test(text)) return "ingredient";
    if (preferred.includes("drinkware") || /(水筒|ボトル|タンブラー|マグボトル|保温ボトル)/.test(text)) return "drinkware";
    if (/(カフェインレス|ノンカフェイン|カモミール|ハーブティー|ルイボス|おやすみ|リラックス)/.test(text) && (policyKey === "shizumeru" || policyKey === "yurumeru")) return "caffeine_shift";
    return "warm_drink";
  }

  if (category === "live") {
    if (/(アイマスク|耳栓|遮光|遮音|ブルーライト)/.test(text)) return policyKey === "sleep" ? "sleep_environment" : "reduce_light";
    if (/(枕|まくら|ピロー|pillow|睡眠|リカバリーウェア|寝室|寝具|マットレス)/.test(text)) return "sleep_environment";
    if (/(腹巻|湯たんぽ|レッグウォーマー|ネックウォーマー|温熱|カイロ|足湯)/.test(text)) return "warm_body";
    if (/(入浴剤|バスソルト|炭酸|温浴|足湯)/.test(text)) return "bath_shift";
    if (/(除湿|湿気|防湿|ドライ)/.test(text)) return "humidity_control";
    if (/(加湿|保湿|マスク|乾燥)/.test(text)) return "moisture_air";
    if (/(温湿度計|室内|環境)/.test(text)) return "sleep_environment";
    return "general";
  }

  if (category === "point") {
    if (POINT_BEAUTY_REJECT_PATTERN.test(text)) return "general";
    if (/(枕|まくら|ピロー|pillow|寝具|マットレス|睡眠)/.test(text)) return "sleep_environment";
    if (/(温熱|温め|あったか|ホット|発熱|遠赤外線|カイロ|パッド|パット|貼る|湿熱|温活)/.test(text)) return "heat_release";
    if (/(首|肩|肩甲骨|マッサージボール)/.test(text)) return "neck_shoulder_release";
    if (/(フォームローラー|ストレッチポール|ヨガマット|姿勢|背中|腰)/.test(text)) return "posture_release";
    if (/(ふくらはぎ|足裏|足首|足元|フット|レッグ)/.test(text)) return "foot_leg_release";
    if (/(ストレッチバンド|やわらかい|伸ばす)/.test(text)) return "gentle_stretch";
    if (/(ツボ|お灸|せんねん灸|電子灸)/.test(text)) return "tsubo_support";
    if (/(頭皮|目元|アイマスク)/.test(text)) return "reduce_light";
  }

  return "general";
}

function buildQueryPlans({ category, policyKeys, symptomKey, priceBand, basis, lifeKeys, limit }) {
  const safeCategory = CATEGORY_LABELS[category] ? category : "live";
  const safePolicyKeys = asArray(policyKeys).filter((key) => POLICY_LABELS[key]).slice(0, 3);
  const primaryPolicyKey = safePolicyKeys[0] || "sasaeru";
  const requestedLimit = clampNumber(limit, 12, 8, 24);
  const planLimit = Math.min(8, Math.max(5, Math.ceil(requestedLimit / 2) + 2));
  const plans = [];
  const seenKeywords = new Set();
  const lifeRows = lifeRowsFor(lifeKeys, safeCategory);

  function addPlanFromRow(row, { policyKey, source, sourceKey }) {
    const normalized = normalizeQueryRow(row);
    const keyword = cleanKeyword(normalized?.keyword);
    if (!keyword || seenKeywords.has(keyword)) return false;

    if (safeCategory === "eat" && !canAddEatPlan(plans, normalized)) return false;

    const resolvedPolicyKey = source === "life"
      ? resolveLifePolicyKey(normalized, safePolicyKeys, getPolicyKey(policyKey, primaryPolicyKey))
      : getPolicyKey(policyKey, primaryPolicyKey);
    const productRole = inferProductRole({ category: safeCategory, normalized, policyKey: resolvedPolicyKey });
    const productRoleLabel = PRODUCT_ROLE_META[productRole]?.label || "ケア候補";
    const tags =
      source === "symptom" || source === "life"
        ? uniqueStrings([productRoleLabel, ...asArray(normalized.tags)]).slice(0, 3)
        : uniqueStrings([productRoleLabel, ...asArray(normalized.tags)]).slice(0, 3);

    plans.push({
      keyword,
      reason: normalized.reason,
      tags,
      policyKey: resolvedPolicyKey,
      source,
      sourceKey: sourceKey || (source === "symptom" ? "symptom" : resolvedPolicyKey),
      category: safeCategory,
      strictBeverage: Boolean(normalized.strictBeverage),
      qualityTeaSearch: Boolean(normalized.qualityTeaSearch),
      intentType: normalized.intentType || null,
      productRole,
      productRoleLabel,
      preferredProductTypes: asArray(normalized.preferredProductTypes),
      allowedProductTypes: asArray(normalized.allowedProductTypes),
      avoidProductTypes: asArray(normalized.avoidProductTypes),
      basis: basis || null,
    });

    seenKeywords.add(keyword);
    return true;
  }

  // 1本目: 生活サインは「方針別検索の材料」ではなく、生活サイン自身の文脈で1本だけ先に立てる。
  // これで味噌汁・スープのような中立アイテムが、たまたま別方針のラベルを背負う事故を減らす。
  if (lifeRows.length) {
    addPlanFromRow(lifeRows[0], { policyKey: primaryPolicyKey, source: "life", sourceKey: "life" });
  }

  const primaryContextRows = contextRowsFor(symptomKey, primaryPolicyKey, safeCategory);
  const primaryCandidates = [
    ...primaryContextRows,
    ...(safeCategory === "eat" ? eatPolicyDiversityRowsFor(primaryPolicyKey) : []),
    ...categoryAnchorRowsFor(safeCategory, primaryPolicyKey, symptomKey),
    ...fallbackSymptomRowsFor(symptomKey, safeCategory),
    ...fallbackPolicyRowsFor(primaryPolicyKey, safeCategory),
    ...(safeCategory === "eat" ? yakuzenBlendRowsFor(primaryPolicyKey, symptomKey, priceBand).slice(0, 1) : []),
  ];
  for (const row of primaryCandidates) {
    if (plans.length >= planLimit) break;
    if (addPlanFromRow(row, { policyKey: primaryPolicyKey, source: "symptom", sourceKey: "symptom" })) break;
  }

  // 2本目以降: 方針別。生活サイン行はここで再利用しない。
  // 薬膳茶・和漢茶は候補として残すが、方針ごとに最大1本までに抑える。
  safePolicyKeys.forEach((policyKey) => {
    if (plans.length >= planLimit) return;

    const contextRows = contextRowsFor(symptomKey, policyKey, safeCategory);
    const diversityRows = safeCategory === "eat" ? eatPolicyDiversityRowsFor(policyKey) : [];
    const yakuzenRows = safeCategory === "eat" ? yakuzenBlendRowsFor(policyKey, symptomKey, priceBand).slice(0, 1) : [];
    const candidates = [
      ...(policyKey === primaryPolicyKey ? contextRows.slice(1) : contextRows),
      ...diversityRows,
      ...categoryAnchorRowsFor(safeCategory, policyKey, symptomKey),
      ...fallbackPolicyRowsFor(policyKey, safeCategory),
      ...fallbackSymptomRowsFor(symptomKey, safeCategory),
      ...yakuzenRows,
      ...contextRows,
    ];

    let addedForPolicy = 0;
    for (const row of candidates) {
      if (plans.length >= planLimit || addedForPolicy >= 2) break;
      if (addPlanFromRow(row, { policyKey, source: "policy", sourceKey: policyKey })) {
        addedForPolicy += 1;
      }
    }
  });

  // 食べるタブは、飲み物だけに寄らないよう最低限の軽食検索を1本足す。
  if (safeCategory === "eat" && countPlansByIntent(plans, "light_meal") === 0) {
    const fallbackLightMeal = eatPolicyDiversityRowsFor(primaryPolicyKey)[0] ||
      careQueryRow("味噌汁 フリーズドライ", "食事を考える余力が少ない時に、温かく軽く足しやすい候補です。", ["軽食", "温かい"], { intentType: "light_meal", preferredProductTypes: ["soupMeal"], avoidProductTypes: ["teaBlend"] });
    addPlanFromRow(fallbackLightMeal, { policyKey: primaryPolicyKey, source: "policy", sourceKey: primaryPolicyKey });
  }

  // 生活サインが複数ある時は、最初のサインだけで棚が固まらないよう追加で拾う。
  if (lifeRows.length > 1) {
    for (const row of lifeRows.slice(1)) {
      if (plans.length >= planLimit) break;
      addPlanFromRow(row, { policyKey: primaryPolicyKey, source: "life", sourceKey: "life" });
    }
  }

  return plans.slice(0, planLimit);
}

function firstImageUrl(item) {
  const candidates = [
    item?.mediumImageUrls,
    item?.smallImageUrls,
    item?.imageUrls,
  ];

  for (const list of candidates) {
    if (!Array.isArray(list)) continue;
    for (const entry of list) {
      if (typeof entry === "string" && entry) return entry.replace("?_ex=128x128", "");
      if (entry?.imageUrl) return String(entry.imageUrl).replace("?_ex=128x128", "");
    }
  }

  return "";
}

