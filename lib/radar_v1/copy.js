export function getSignalLabel(signal) {
  if (signal === 2) return "\u8981\u8b66\u6212";
  if (signal === 1) return "\u6ce8\u610f";
  return "\u5b89\u5b9a";
}

export function getTriggerLabel(mainTrigger, triggerDir) {
  if (mainTrigger === "pressure" && triggerDir === "down") {
    return "\u6c17\u5727\u304c\u4e0b\u304c\u308b\u65e5";
  }
  if (mainTrigger === "pressure" && triggerDir === "up") {
    return "\u6c17\u5727\u304c\u4e0a\u304c\u308b\u65e5";
  }
  if (mainTrigger === "temp" && triggerDir === "down") {
    return "\u51b7\u3048\u8fbc\u307f\u3084\u3059\u3044\u65e5";
  }
  if (mainTrigger === "temp" && triggerDir === "up") {
    return "\u6c17\u6e29\u304c\u4e0a\u304c\u308a\u3084\u3059\u3044\u65e5";
  }
  if (mainTrigger === "humidity" && triggerDir === "up") {
    return "\u6e7f\u304c\u3053\u3082\u308a\u3084\u3059\u3044\u65e5";
  }
  return "\u6c17\u8c61\u306e\u5909\u5316\u304c\u3042\u308b\u65e5";
}

